//SET COLOR OF PIECES
void bcolor(int x , int y){
    if((x+y+1)%2==0){
        //bg-black
        printf("\033[0m\033[30;42m");
    }
    else{
        //bg-white
        printf("\033[0m\033[30;100m");
    }
}
void wcolor(int x , int y){
    if((x+y+1)%2==0){
        //bg-black
        printf("\033[0m\033[37;42m"); 
    }
    else{
        //bg-white
        printf("\033[0m\033[37;100m");
    }
}


void display(char data[8][8][4]){
    printf("\033[2J\033[H"); 
    for(int h=0;h<8;h++){
        for(int v=0;v<8;v++){
            if(data[h][v][0]=='P'){
                wcolor(h,v);
                pawnren(h,v);
            }
            else if(data[h][v][0]=='N'){
                wcolor(h,v);
                knightren(h,v);
            }
            else if(data[h][v][0]=='B'){
                wcolor(h,v);
                bishopren(h,v);
            }
            else if(data[h][v][0]=='R'){
                wcolor(h,v);
                rookren(h,v);
            }
            else if(data[h][v][0]=='Q'){
                wcolor(h,v);
                queenren(h,v);
            }
            else if(data[h][v][0]=='K'){
                wcolor(h,v);
                kingren(h,v);
            }
            else if(data[h][v][0]=='p'){
                bcolor(h,v);
                pawnren(h,v);
            }
            else if(data[h][v][0]=='n'){
                bcolor(h,v);
                knightren(h,v);
            }
            else if(data[h][v][0]=='b'){
                bcolor(h,v);
                bishopren(h,v);
            }
            else if(data[h][v][0]=='r'){
                bcolor(h,v);
                rookren(h,v);
            }
            else if(data[h][v][0]=='q'){
                bcolor(h,v);
                queenren(h,v);
            }
            else if(data[h][v][0]=='k'){
                bcolor(h,v);
                kingren(h,v);
            }
            else{
                bcolor(h,v);
                emptyren(h,v);
            }
        }
    }
}